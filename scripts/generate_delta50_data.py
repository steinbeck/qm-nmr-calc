def generate_xyz_file(compound_id: int, data: dict, output_dir: Path) -> None:
    """Generate an XYZ file for a compound."""
    atoms = data["atoms"]
    n_atoms = len(atoms)
    name = data["name"]

    filename = f"compound_{compound_id:02d}.xyz"
    filepath = output_dir / filename

    lines = [
        str(n_atoms),
        f"{name} - DELTA50 Compound {compound_id} - B3LYP/6-31G(d) optimized",
    ]

    for atom_type, x, y, z, _ in atoms:
        lines.append(f"{atom_type:2s}  {x:12.6f}  {y:12.6f}  {z:12.6f}")

    with open(filepath, "w") as f:
        f.write("\n".join(lines) + "\n")

    print(f"  Created {filename}")


def generate_experimental_shifts(output_dir: Path) -> None:
    """Generate experimental_shifts.json file."""
    shifts_data = {
        "source": {
            "paper": "Grimblat et al., Molecules 2023, 28(6), 2449",
            "doi": "10.3390/molecules28062449",
            "conditions": "CDCl3, 298K, referenced to TMS at 0.00 ppm",
            "geometry": "B3LYP/6-31G(d) optimized in vacuo"
        },
        "molecules": {}
    }

    for compound_id, data in DELTA50_COMPOUNDS.items():
        mol_id = f"compound_{compound_id:02d}"
        atoms = data["atoms"]

        # Extract shifts by atom type
        c13_shifts = []
        c13_assignments = {}
        h1_shifts = []
        h1_assignments = {}

        for i, (atom_type, x, y, z, shift) in enumerate(atoms, start=1):
            if shift is not None:
                if atom_type == "C":
                    c13_shifts.append(shift)
                    c13_assignments[str(i)] = shift
                elif atom_type == "H":
                    h1_shifts.append(shift)
                    h1_assignments[str(i)] = shift

        shifts_data["molecules"][mol_id] = {
            "name": data["name"],
            "compound_number": compound_id,
            "h1_shifts": sorted(set(h1_shifts)),  # Unique values, sorted
            "c13_shifts": sorted(set(c13_shifts)),  # Unique values, sorted
            "h1_assignments": h1_assignments,
            "c13_assignments": c13_assignments,
            "num_h_atoms": len(h1_assignments),
            "num_c_atoms": len(c13_assignments),
        }

    filepath = output_dir / "experimental_shifts.json"
    with open(filepath, "w") as f:
        json.dump(shifts_data, f, indent=2)

    print(f"Created experimental_shifts.json with {len(shifts_data['molecules'])} molecules")


